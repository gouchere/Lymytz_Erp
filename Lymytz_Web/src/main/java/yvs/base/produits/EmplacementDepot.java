package yvs.base.produits;

import java.io.Serializable;import yvs.entity.compta.YvsBaseCaisse;
import javax.faces.bean.ManagedBean;
import javax.faces.bean.SessionScoped;
import yvs.parametrage.entrepot.Depots;

/**
 *
 * @author hp Elite 8300
 */
@SessionScoped
@ManagedBean
public class EmplacementDepot implements Serializable{    
    private long id;
    private String code;
    private Depots depot=new Depots();
    private String designation;

    public EmplacementDepot() {
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public Depots getDepot() {
        return depot;
    }

    public void setDepot(Depots depot) {
        this.depot = depot;
    }

    public String getDesignation() {
        return designation;
    }

    public void setDesignation(String designation) {
        this.designation = designation;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        hash = 59 * hash + (int) (this.id ^ (this.id >>> 32));
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final EmplacementDepot other = (EmplacementDepot) obj;
        if (this.id != other.id) {
            return false;
        }
        return true;
    }
    
    
    
}
