package yvs.production.base;

import java.io.Serializable;
import java.util.Objects;

/**
 *
 * @author lymytz
 */
public class TypeRessource implements Serializable {

    private String code;
    private String libelle;

    public TypeRessource() {
    }

    public TypeRessource(String code, String libelle) {
        this.code = code;
        this.libelle = libelle;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getLibelle() {
        return libelle;
    }

    public void setLibelle(String libelle) {
        this.libelle = libelle;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        hash = 67 * hash + Objects.hashCode(this.code);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final TypeRessource other = (TypeRessource) obj;
        if (!Objects.equals(this.code, other.code)) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "TypeRessource{" + "code=" + code + ", libelle=" + libelle + '}';
    }

}
