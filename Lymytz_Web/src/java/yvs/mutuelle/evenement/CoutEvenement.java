package yvs.mutuelle.evenement;

import java.io.Serializable;import yvs.entity.compta.YvsBaseCaisse;
import javax.faces.bean.ManagedBean;
import javax.faces.bean.SessionScoped;
import yvs.grh.bean.TypeCout;

/**
 *
 * @author lymytz
 */
@ManagedBean
@SessionScoped
public class CoutEvenement implements Serializable {

    private long id;
    private double montant;
    private Evenement evenement = new Evenement();
    private TypeCout type = new TypeCout();
    private boolean selectActif, new_, update;

    public CoutEvenement() {
    }

    public CoutEvenement(long id) {
        this.id = id;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public double getMontant() {
        return montant;
    }

    public void setMontant(double montant) {
        this.montant = montant;
    }

    public Evenement getEvenement() {
        return evenement;
    }

    public void setEvenement(Evenement evenement) {
        this.evenement = evenement;
    }

    public TypeCout getType() {
        return type;
    }

    public void setType(TypeCout type) {
        this.type = type;
    }

    public boolean isSelectActif() {
        return selectActif;
    }

    public void setSelectActif(boolean selectActif) {
        this.selectActif = selectActif;
    }

    public boolean isNew_() {
        return new_;
    }

    public void setNew_(boolean new_) {
        this.new_ = new_;
    }

    public boolean isUpdate() {
        return update;
    }

    public void setUpdate(boolean update) {
        this.update = update;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        hash = 67 * hash + (int) (this.id ^ (this.id >>> 32));
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final CoutEvenement other = (CoutEvenement) obj;
        if (this.id != other.id) {
            return false;
        }
        return true;
    }

}
